public class StudyProgramme {


    protected String studyProgrammeName;
    protected String description;
    protected int semester;
    protected int itnPossibility;
    public StudyProgramme(String studyProgrammeName, String description, int semester, int itnPossibility) {
        this.studyProgrammeName = studyProgrammeName;
        this.description = description;
        this.semester = semester;
        this.itnPossibility = itnPossibility;
    }

}
